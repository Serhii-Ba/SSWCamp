<?php
/*Напишіть функцію, яка отримує масив цілих
чисел і повертає масив середніх значень кожного
цілого числа та його послідовника, якщо він є.
Приклад:
averages([2, -2, 2, -2, 2]), [0, 0, 0, 0]
averages([1, 3, 5, 1, -10]), [2, 4, 3, -4.5]*/

function fun ($ar) {
    $ret = [];
    $s = count($ar); //підраховує кількість елементів в масиві
    for ($i=0; $i<$s-1; $i++) {
        $ret[]= ($ar[$i]+ $ar[$i+1]) / 2;
    }
    return $ret;
}

print_r(fun ([1, 3, 5, 1, -10, 2, -2, 2, -2, 2]));
